#include "libft.h"

int		ft_atoi(const char *str)
{
	int neg;
	int num;

	num = 0;
	neg = 1;
	while ((*str == '\t' || *str == '\v' || *str == '\f' || *str == '\r' ||
			*str == '\n' || *str == ' ') && *str != '\0')
		str++;
	if (*str == '-' && str[1] >= '0' && str[1] <= '9')
	{
		neg = -1;
		str++;
	}
	if (*str == '+' && str[1] >= '0' && str[1] <= '9')
		str++;
	while (*str != '\0' && *str >= '0' && *str <= '9')
	{
		num *= 10;
		num += *str - '0';
		str++;
	}
	return (num * neg);
}
